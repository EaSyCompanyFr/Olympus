<?php

namespace Easy\SiteBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Membresquad
 *
 * @ORM\Table(name="membresquad")
 * @ORM\Entity
 */
class Membresquad
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer", nullable=false)
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $id;



    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }
}